// Javascript - Tipagem dinâmica
// String - Tipo de dado
console.log('Joao "Sales"'); // String = Texto = Cadeia de caracteres
console.log("Joao 'Sales'");
console.log(`'Joao' "Sales"`); // Template String
console.log(100, 15.85, 'Joao Sales De Novo');
console.log(85); // Number = Número
console.log(20.1234); // Number
